/**
 * Helper function to get rules for specific market pair
 */
export const getOfevRules = (sourceMarket, targetMarket) => {
  return OfevRegulatoryRules.filter(
    rule => rule.sourceMarket === sourceMarket && rule.targetMarket === targetMarket
  );
};

/**
 * Helper function to get rules by type
 */
export const getOfevRulesByType = (sourceMarket, targetMarket, ruleType) => {
  return OfevRegulatoryRules.filter(
    rule =>
      rule.sourceMarket === sourceMarket &&
      rule.targetMarket === targetMarket &&
      rule.ruleType === ruleType
  );
};

/**
 * Convert Ofev rules to RegulatoryMatrixEntry format for service integration
 */
export const convertOfevRulesToMatrixEntries = (rules) => {
  return rules.map(rule => ({
    market: rule.targetMarket,
    therapeuticArea: 'Respiratory',
    ruleCategory: rule.category,
    ruleId: rule.id,
    ruleName: rule.title,
    changeRequirement: rule.ruleType,
    description: rule.description,
    rationale: rule.rationale,
    riskLevel: rule.severity,
    validationMethod: rule.requiresMLRApproval ? 'expert_review' : 'automated',
    preApprovedContent: rule.preApprovedReplacement ? [rule.preApprovedReplacement] : undefined,
    templateContent: rule.suggestedReplacement
  }));
};