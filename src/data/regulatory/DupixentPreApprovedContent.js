/**
 * Helper function to get pre-approved content by market
 */

export function getDupixentContentByMarket(market) {
  return DupixentPreApprovedContent.filter(content => content.market === market);
}

export function getDupixentContentByCategory(
  market, 
  category
) {
  return DupixentPreApprovedContent.filter(
    content => content.market === market && content.category === category
  );
}

export function searchDupixentContent(searchTerm) {
  const lowerSearch = searchTerm.toLowerCase();
  return DupixentPreApprovedContent.filter(
    content =>
      content.approvedContent.toLowerCase().includes(lowerSearch) ||
      content.contentType.toLowerCase().includes(lowerSearch) ||
      content.category.toLowerCase().includes(lowerSearch)
  );
}