/**
 * Main function to retrieve Xarelto rules by market pair
 */
export const getXareltoRules = (sourceMarket, targetMarket) => {
  const marketKey = `${sourceMarket.toUpperCase()}_${targetMarket.toUpperCase()}`;
  
  console.log(`[Xarelto] Fetching regulatory rules for ${sourceMarket}→${targetMarket}`);
  
  switch (marketKey) {
    case 'US_GERMANY':
    case 'US_DE':
      return usToGermanyRules;
    case 'US_FRANCE':
    case 'US_FR':
      return usToFranceRules;
    case 'US_CHINA':
    case 'US_CN':
      return usToChinaRules;
    default:
      console.warn(`[Xarelto] No rules found for market pair: ${marketKey}`);
      return [];
  }
};

/**
 * Get rules by rule type (MUST_CHANGE, CANNOT_CHANGE, SHOULD_CHANGE)
 */
export const getXareltoRulesByType = (sourceMarket, targetMarket, ruleType) => {
  const allRules = getXareltoRules(sourceMarket, targetMarket);
  return allRules.filter(rule => rule.ruleType === ruleType);
};

/**
 * Convert Xarelto-specific rules to generic RegulatoryMatrixEntry format
 * Used by RegulatoryIntelligenceMatrixService.js
 */
export const convertXareltoRulesToMatrixEntries = (rules) => {
  return rules.map(rule => ({
    market: rule.targetMarket,
    therapeuticArea: 'Cardiovascular',
    ruleCategory: rule.category,
    ruleId: rule.id,
    ruleName: rule.title,
    changeRequirement: rule.ruleType,
    riskLevel: rule.severity,
    description: rule.description,
    rationale: rule.rationale,
    suggestedAction: rule.preApprovedReplacement || rule.suggestedReplacement || rule.description,
    requiresMLRApproval: rule.requiresMLRApproval,
    brandId: rule.brandId,
    validationMethod: rule.regex ? 'automated' : 'manual'
  }));
};