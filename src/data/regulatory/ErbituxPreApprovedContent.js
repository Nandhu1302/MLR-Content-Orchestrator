/**
 * Helper function to get Erbitux content by market
 * @param {string} market
 * @returns {Array}
 */
export function getErbituxContentByMarket(market) {
  return ErbituxPreApprovedContent.filter(content => content.market === market);
}

/**
 * Helper function to get Erbitux content by category
 * @param {string} market
 * @param {string} category
 * @returns {Array}
 */
export function getErbituxContentByCategory(
  market,
  category
) {
  return ErbituxPreApprovedContent.filter(
    content => content.market === market && content.category === category
  );
}

/**
 * Search content
 * @param {string} searchTerm
 * @returns {Array}
 */
export function searchErbituxContent(searchTerm) {
  const lowerSearch = searchTerm.toLowerCase();
  return ErbituxPreApprovedContent.filter(
    content =>
      content.approvedContent.toLowerCase().includes(lowerSearch) ||
      content.contentType.toLowerCase().includes(lowerSearch)
  );
}