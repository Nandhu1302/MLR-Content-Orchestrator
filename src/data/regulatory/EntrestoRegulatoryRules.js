/**
 * Helper function to get rules for specific market pair
 * @param {string} sourceMarket
 * @param {string} targetMarket
 */
export const getEntrestoRules = (sourceMarket, targetMarket) => {
  return EntrestoRegulatoryRules.filter(
    rule => rule.sourceMarket === sourceMarket && rule.targetMarket === targetMarket
  );
};

/**
 * Helper function to get rules by type
 * @param {string} sourceMarket
 * @param {string} targetMarket
 * @param {'MUST_CHANGE' | 'CANNOT_CHANGE' | 'SHOULD_CHANGE'} ruleType
 */
export const getEntrestoRulesByType = (
  sourceMarket,
  targetMarket,
  ruleType
) => {
  return EntrestoRegulatoryRules.filter(
    rule =>
      rule.sourceMarket === sourceMarket &&
      rule.targetMarket === targetMarket &&
      rule.ruleType === ruleType
  );
};

/**
 * Convert Entresto rules to RegulatoryMatrixEntry format for service integration
 * @param {Array} rules
 */
export const convertEntrestoRulesToMatrixEntries = (rules) => {
  return rules.map(rule => ({
    market: rule.targetMarket,
    therapeuticArea: 'Cardiovascular',
    ruleCategory: rule.category,
    ruleId: rule.id,
    ruleName: rule.title,
    changeRequirement: rule.ruleType,
    riskLevel: rule.severity,
    description: rule.description,
    rationale: rule.rationale,
    suggestedAction: rule.preApprovedReplacement || rule.suggestedReplacement || rule.description,
    requiresMLRApproval: rule.requiresMLRApproval,
    brandId: rule.brandId,
    validationMethod: rule.regex ? 'automated' : 'manual'
  }));
};